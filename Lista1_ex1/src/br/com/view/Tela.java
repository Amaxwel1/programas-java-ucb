    private void jBcalcularCustoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcalcularCustoActionPerformed
        // TODO add your handling code here:
         if(jTcustoFabrica.getText().equals("")){
             JOptionPane.showMessageDialog(rootPane, "campo obrigatorio!");
             jTcustoFabrica.requestFocus();
          }
         else {
        c.setCustoFabrica(Double.parseDouble(jTcustoFabrica.getText()));
        jLmostrarCusto.setText("R$ "+jTcustoFabrica.getText());
        if(jRdesconto.isSelected()){
           jBcalcularCusto.setText("R$ "+ ((c.getCustoFabrica()) + (c.calcularCusto()*0.2)));
        } else
           jBcalcularCusto.setText("R$ "+ c.calcularCusto());
  
        jTcustoFabrica.setText("");
        jTcustoFabrica.requestFocus();

       } 
    }//GEN-LAST:event_jBcalcularCustoActionPerformed

